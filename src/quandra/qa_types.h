
#ifndef __QUANDRA_TYPES_H__
#define __QUANDRA_TYPES_H__

#include <stdint.h>
#include <stdbool.h>

typedef int8_t qa_int8;
typedef int16_t qa_int16;
typedef int32_t qa_int32;
typedef int64_t qa_int64;

typedef uint8_t qa_uint8;
typedef uint16_t qa_uint16;
typedef uint32_t qa_uint32;
typedef uint64_t qa_uint64;

typedef float qa_real32;
typedef double qa_real64;

typedef char  qa_char8;
typedef qa_int16 qa_char16;
typedef qa_char8 qa_char;

#endif
